import { Helmet } from 'react-helmet-async';

const SEOHead = ({ 
  title = "Candela Public School | Best CBSE School in Mumbai", 
  description = "Top-rated CBSE school in Mumbai offering quality education from Playgroup to Class 12. Modern facilities, experienced teachers, holistic development.",
  keywords = "Candela Public School, Best CBSE School Mumbai, Top School Mumbai, English Medium School",
  canonical = "https://candelapublicschool.com",
  ogImage = "https://candelapublicschool.com/og-image.jpg",
  structuredData = null,
  breadcrumbs = null
}) => {
  return (
    <Helmet>
      {/* Primary Meta Tags */}
      <title>{title}</title>
      <meta name="title" content={title} />
      <meta name="description" content={description} />
      <meta name="keywords" content={keywords} />
      
      {/* Open Graph */}
      <meta property="og:title" content={title} />
      <meta property="og:description" content={description} />
      <meta property="og:image" content={ogImage} />
      <meta property="og:url" content={canonical} />
      
      {/* Twitter */}
      <meta name="twitter:title" content={title} />
      <meta name="twitter:description" content={description} />
      <meta name="twitter:image" content={ogImage} />
      
      {/* Canonical */}
      <link rel="canonical" href={canonical} />
      
      {/* Structured Data */}
      {structuredData && (
        <script type="application/ld+json">
          {JSON.stringify(structuredData)}
        </script>
      )}
      
      {/* Breadcrumbs Schema */}
      {breadcrumbs && (
        <script type="application/ld+json">
          {JSON.stringify({
            "@context": "https://schema.org",
            "@type": "BreadcrumbList",
            "itemListElement": breadcrumbs.map((crumb, index) => ({
              "@type": "ListItem",
              "position": index + 1,
              "name": crumb.name,
              "item": crumb.url
            }))
          })}
        </script>
      )}
    </Helmet>
  );
};

export default SEOHead;
